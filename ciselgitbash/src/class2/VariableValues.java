package class2;

public class VariableValues {
    public static void main(String[] args) {

        byte numberPens=2;
        numberPens=4;
        System.out.println(numberPens);
        short numberClasses=35;
        numberClasses=60;
        System.out.println(numberClasses);
        int age=28;
        age=30;
        System.out.println(age);
        long boxes=656989898L;
        boxes=789586289L;
        System.out.println(boxes);
        float salary=456.7F;
        salary=589.9F;
        System.out.println(salary);
        double gross=123456.5;
        gross=45689.8;
        System.out.println(gross);
        char blue='B';
        blue='L';
        System.out.println(blue);
        boolean yes=true;
        yes=false;
        System.out.println(yes);

        String city="London";
        city="Istanbul";
        System.out.println(city);



    }
}
